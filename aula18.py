# Utilizamos o Debugger nesta aula para ver como funciona um código passo a passo.

condicao1 = True
condicao2 = True
condicao3 = False
condicao4 = True

if condicao1:
    print('Código para condicao 1')
elif condicao2:
    print('código para condicao 2')
elif condicao3:    
    print('código para condicao 3')
elif condicao4:
    print('código para condicao 4')

else:
    print('nenhuma condição foi satisfeita')
